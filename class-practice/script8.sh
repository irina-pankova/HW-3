#!/bin/bash

# Устанавливаем порог
THRESHOLD=80

# Получаем процент использования корневого раздела (/)
usage=$(df / | awk 'NR==2 {print $5}' | sed 's/%//')

echo "Текущее использование диска: ${usage}%"

# Проверяем, превышает ли использование порог
if [ "$usage" -gt "$THRESHOLD" ]; then
    echo "⚠️  Внимание: использование диска превышает ${THRESHOLD}%!"
else
    echo "✅ Использование диска в норме."
fi
