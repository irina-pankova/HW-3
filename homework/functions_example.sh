#!/bin/bash

# --- Функция 1: приветствие ---
# Принимает 1 аргумент — строку (имя)
# Выводит строку с префиксом "Hello, "
say_hello() {
  local name="$1"  # $1 — первый аргумент функции
  echo "Hello, $name!"
}

# --- Функция 2: сумма двух чисел ---
# Принимает 2 аргумента — числа
# Возвращает их сумму
sum_two_numbers() {
  local a="$1"
  local b="$2"
  local sum=$((a + b))  # арифметическая операция
  echo "$sum"            # выводим результат
}

# --- Основная часть скрипта ---

# Вызов первой функции
read -p "Введите ваше имя: " user_name
say_hello "$user_name"

# Вызов второй функции
read -p "Введите первое число: " num1
read -p "Введите второе число: " num2

result=$(sum_two_numbers "$num1" "$num2")
echo "Сумма чисел $num1 и $num2 равна $result."
