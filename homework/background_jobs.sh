#!/bin/bash

echo "Запускаем три команды sleep в фоне..."

sleep 5 &
sleep 10 &
sleep 15 &

echo "Все три sleep запущены в фоновом режиме."
echo
echo "Проверим список фоновых задач:"
jobs

echo
echo "Подождём немного, чтобы посмотреть, как процессы завершаются..."
sleep 2

echo
echo "Снова проверим список фоновых задач:"
jobs

echo
echo "Вы можете вручную использовать команды:"
echo "  fg %1   — вывести первую задачу на передний план"
echo "  bg %1   — вернуть её в фон"
echo
echo "Ожидание завершения всех процессов..."
wait
echo "Все процессы завершены."
