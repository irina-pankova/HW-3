#!/bin/bash

# Разрешаем использование alias внутри скрипта
shopt -s expand_aliases

# Создаём alias
alias ll='ls -la'

echo "Демонстрация работы alias ll:"
ll

echo "Переход в домашнюю директорию:"
cd ~
pwd

# Демонстрация автодополнения в скрипте невозможна —
# она работает только в интерактивном режиме (через Tab).
