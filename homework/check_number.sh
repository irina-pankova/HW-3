#!/bin/bash

# Запрос числа у пользователя
read -p "Введите число: " num

# Проверяем, является ли введённое значение числом
if ! [[ "$num" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: введите целое число."
  exit 1
fi

# Проверка знака числа
if [ "$num" -gt 0 ]; then
  echo "Число положительное."
elif [ "$num" -lt 0 ]; then
  echo "Число отрицательное."
else
  echo "Это ноль."
fi

# Если число положительное — считаем от 1 до введённого числа
if [ "$num" -gt 0 ]; then
  i=1
  echo "Счёт от 1 до $num:"
  while [ $i -le $num ]; do
    echo $i
    ((i++))
  done
fi
